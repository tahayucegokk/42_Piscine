int	ft_strcmp(char *s1, char *s2)
{
	int	u;

	u = 0;
	while (s1[u] != '\0' || s2[u] != '\0')
	{
		if (s1[u] > s2[u])
			return (1);
		else if (s1[u] < s2[u])
			return (-1);
		u++;
	}
	return (0);
}
